export function renderSprite(ctx, spriteData, palette, x, y, scale = 1) {
  for (let row = 0; row < spriteData.length; row++) {
    for (let col = 0; col < spriteData[row].length; col++) {
      const colorKey = spriteData[row][col];
      if (colorKey === '_') continue;
      ctx.fillStyle = palette[colorKey] || PALETTE[colorKey];
      ctx.fillRect(x + col * scale, y + row * scale, scale, scale);
    }
  }
}

export function renderAll(ctx, x, y, scale = 1) {
  renderSprite(ctx, ANGEL, PALETTE, x, y, scale);
}
