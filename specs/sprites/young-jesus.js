export function renderSprite(ctx, x, y, scale = 1) {
  for (let row = 0; row < YOUNG_JESUS.length; row++) {
    for (let col = 0; col < YOUNG_JESUS[row].length; col++) {
      const colorKey = YOUNG_JESUS[row][col];
      if (colorKey === '_') continue;
      ctx.fillStyle = PALETTE[colorKey];
      ctx.fillRect(x + col * scale, y + row * scale, scale, scale);
    }
  }
}
